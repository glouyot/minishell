#include "../include/builtins.h"

int     main(int ac, char **av)
{
    int i;

    i = 0;
    while(i++ < ac - 1)
    {
        ft_putstr(av[i]);
        if (i < ac - 1)
            ft_putchar(' ');
    }
    ft_putendl("");
}
